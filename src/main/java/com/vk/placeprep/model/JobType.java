package com.vk.placeprep.model;

/**
 * Represents the type of employment being offered or applied for.
 * Used to categorize job positions and filter interview experiences.
 * 
 * @author Venkatesh K
 * @version 1.0
 * @since 2025-10-26
 */
public enum JobType {
    /**
     * Internship position, typically for students or recent graduates.
     * Usually temporary and focused on providing work experience.
     */
    INTERNSHIP,
    
    /**
     * Full-time employment position.
     * Represents a regular, permanent position with the company.
     */
    FULL_TIME
}
